def seed_phase_2_test_ideas(project_root: Path) -> None:
    """Run the Phase 2 pipeline on the flawed-requirements fixture so
    `<workspace>/test-ideas/REQ-NNN.md` files exist as Phase 4's input."""
    workspace = project_root / ".test-commander"
    uploaded = workspace / "documents" / "uploaded"
    uploaded.mkdir(parents=True, exist_ok=True)
    shutil.copy(FLAWED_REQUIREMENTS, uploaded / FLAWED_REQUIREMENTS.name)
    # Import the Phase 2 helpers directly (they are pure Python and
    # already used by tests/test_requirements_to_tests.py).
    sys.path.insert(0, str(SCRIPTS))
    try:
        import requirements_to_tests
        import review_requirements

        review_requirements.review(project_root)
        requirements_to_tests.to_tests(project_root)
    finally:
        if str(SCRIPTS) in sys.path:
            sys.path.remove(str(SCRIPTS))


def seed_full_pipeline(project_root: Path) -> str:
    """End-to-end setup: workspace + Phase 3 stub + charter + explore +
    session-summary + Phase 2 test-idea seeds. Returns the SESS-ID."""
    run_init(project_root)
    seed_minimal_phase_3_state(project_root)
    run_create_charter(project_root, CHARTER_TARGET)
    recorded_dir = workspace_file(project_root, "documents/uploaded/recorded-sessions")
    recorded_dir.mkdir(parents=True, exist_ok=True)
    shutil.copy(FIXTURE_RECORDED_SESSION, recorded_dir / "CH-001.json")
    run_explore(project_root, "CH-001")
    notes = list(workspace_file(project_root, "exploration-notes").glob("SESS-*.md"))
    assert len(notes) == 1, f"expected exactly one exploration note; got {len(notes)}"
    session_id = notes[0].stem
    run_session_summary(project_root, session_id)
    seed_phase_2_test_ideas(project_root)
    return session_id


