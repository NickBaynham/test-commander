def test_frontmatter_keys_preserved_byte_for_byte(tmp_path: Path) -> None:
    """For every enriched file, every Phase-2 frontmatter key must still
    be present after enrichment with its original value -- except
    ``status:`` which flips ``seed`` -> ``enriched``."""
    session_id = seed_full_pipeline(tmp_path)
    test_ideas = workspace_file(tmp_path, "test-ideas")
    pre_snapshot: dict[str, str] = {}
    for p in sorted(test_ideas.glob("REQ-*.md")):
        pre_snapshot[p.name] = p.read_text(encoding="utf-8")

    run_helper(tmp_path, "--session", session_id)

    enriched_paths = list_enriched_files(tmp_path)
    assert enriched_paths, "at least one file must be enriched for this test to matter"
    for path in enriched_paths:
        original_fm, _ = split_frontmatter(pre_snapshot[path.name])
        new_fm, _ = split_frontmatter(path.read_text(encoding="utf-8"))
        original_keys = frontmatter_top_level_keys(original_fm)
        new_keys = frontmatter_top_level_keys(new_fm)
        for key in original_keys:
            assert key in new_keys, (
                f"{path.name}: frontmatter key {key!r} disappeared after enrichment"
            )
        # Every Phase-2 contract key the seed shipped with must still exist.
        for key in PHASE_2_FRONTMATTER_KEYS:
            if key in original_keys:
                assert key in new_keys, (
                    f"{path.name}: Phase-2 contract key {key!r} missing post-enrich"
                )
        # Scalar values must be byte-identical for every preserved key
        # other than ``status:``.
        preserved_scalars = ("schema", "requirement_id", "requirement_title",
                             "source", "ac_review_present", "generated_by")
        for key in preserved_scalars:
            orig = frontmatter_scalar(original_fm, key)
            curr = frontmatter_scalar(new_fm, key)
            if orig is not None:
                assert curr == orig, (
                    f"{path.name}: scalar {key!r} value mutated "
                    f"({orig!r} -> {curr!r})"
                )


def test_status_flips_on_enriched_only(tmp_path: Path) -> None:
    session_id = seed_full_pipeline(tmp_path)
    run_helper(tmp_path, "--session", session_id)
    test_ideas = workspace_file(tmp_path, "test-ideas")
    enriched_names = {p.name for p in list_enriched_files(tmp_path)}
    for path in sorted(test_ideas.glob("REQ-*.md")):
        fm, _ = split_frontmatter(path.read_text(encoding="utf-8"))
        status = frontmatter_scalar(fm, "status")
        if path.name in enriched_names:
            assert status == "enriched", (
                f"{path.name}: enriched file must carry status: enriched (got {status!r})"
            )
        else:
            assert status == "seed", (
                f"{path.name}: non-enriched file must keep status: seed (got {status!r})"
            )


def test_phase_4_sessions_populated_and_sorted(tmp_path: Path) -> None:
    """``phase_4_sessions:`` must be a YAML inline list containing the
    contributing SESS-IDs, sorted and deduplicated."""
    session_id = seed_full_pipeline(tmp_path)
    run_helper(tmp_path, "--session", session_id)
    for path in list_enriched_files(tmp_path):
        fm, _ = split_frontmatter(path.read_text(encoding="utf-8"))
        sessions_line = frontmatter_scalar(fm, "phase_4_sessions")
        assert sessions_line is not None, (
            f"{path.name}: enriched file missing phase_4_sessions: key"
        )
        # Inline list: ``[SESS-..., ...]``.
        assert sessions_line.startswith("[") and sessions_line.endswith("]"), (
            f"{path.name}: phase_4_sessions must be inline list (got {sessions_line!r})"
        )
        ids = [s.strip() for s in sessions_line.strip("[]").split(",") if s.strip()]
        assert session_id in ids, (
            f"{path.name}: phase_4_sessions does not include {session_id}"
        )
        assert ids == sorted(set(ids)), (
            f"{path.name}: phase_4_sessions not sorted-deduplicated ({ids})"
        )


