def test_idempotent_rerun_no_duplicate_enrichment_sections(tmp_path: Path) -> None:
    session_id = seed_full_pipeline(tmp_path)
    run_helper(tmp_path, "--session", session_id)
    run_helper(tmp_path, "--session", session_id)
    for path in list_enriched_files(tmp_path):
        text = path.read_text(encoding="utf-8")
        section_count = text.count("## Phase 4 enrichment")
        assert section_count == 1, (
            f"{path.name}: expected exactly 1 '## Phase 4 enrichment' header; "
            f"got {section_count}"
        )
        # And the session sub-block must appear exactly once.
        block_count = text.count(f"### {session_id}")
        assert block_count <= 1, (
            f"{path.name}: SESS-ID sub-block duplicated on re-run "
            f"(count={block_count})"
        )


def test_user_edits_to_body_preserved(tmp_path: Path) -> None:
    """A user (or downstream phase) may add a custom section to an enriched
    test-idea between runs. Re-running enrichment must not strip it."""
    session_id = seed_full_pipeline(tmp_path)
    run_helper(tmp_path, "--session", session_id)
    enriched = list_enriched_files(tmp_path)
    assert enriched, "need at least one enriched file for this test"
    target = enriched[0]
    sentinel = "## User-added notes\n\nThis section was authored by hand.\n"
    with target.open("a", encoding="utf-8") as fh:
        fh.write("\n" + sentinel)
    run_helper(tmp_path, "--session", session_id)
    after = target.read_text(encoding="utf-8")
    assert sentinel.rstrip() in after, (
        f"{target.name}: user-edited section stripped on enrichment re-run"
    )


