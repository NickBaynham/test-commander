"""Step 4.5 - /tc:test-ideas (enrich_test_ideas) end-to-end tests.

Drives ``enrich_test_ideas.py`` against a tmp consuming project seeded
with: (a) the Phase 3 product-knowledge minimum needed for /tc:create-charter
to succeed, (b) a CH-001 charter generated by /tc:create-charter, (c) the
seeded exploration recording driven through /tc:explore, (d) the resulting
session summary written by /tc:session-summary, AND (e) the Phase 2 flawed-
requirements fixture passed through /tc:review-requirements + /tc:requirements-
to-tests so seeded test-idea files exist at ``<workspace>/test-ideas/REQ-NNN.md``.

Asserts every contract item enumerated in planning/plan.md Step 4.5:

- uninitialized workspace refused;
- missing session summaries refused with a precondition error pointing at
  ``/tc:session-summary``;
- missing test-idea seeds refused with a precondition error pointing at
  ``/tc:requirements-to-tests``;
- seeded fixture enriches **at least 3** Phase-2 test-ideas with session-
  derived candidate scenarios (per the Step 4.5 verification contract);
- every Phase-2 frontmatter key the seed shipped with is present byte-for-
  byte after enrichment (no key missing, no value mutated);
- ``status: seed`` flips to ``status: enriched`` only on enriched files;
- unenriched files remain ``status: seed`` and carry no ``phase_4_sessions:``
  or ``## Phase 4 enrichment`` section;
- ``phase_4_sessions:`` list contains the contributing SESS-IDs, sorted and
  deduplicated;
- idempotent re-run is byte-identical AND produces zero duplicate enrichment
  sections;
- a user-edited body section added between enrichment runs is preserved;
- when two sessions enrich the same REQ, ``phase_4_sessions:`` carries both
  SESS-IDs sorted and the body has one ``## Phase 4 enrichment`` section
  per session (or one section with per-session sub-blocks).
"""

