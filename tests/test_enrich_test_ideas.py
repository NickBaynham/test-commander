def test_multiple_sessions_merged_into_phase_4_sessions(tmp_path: Path) -> None:
    """When two sessions enrich the same REQ, ``phase_4_sessions:`` carries
    both sorted-deduplicated, and the body has two SESS-ID sub-blocks."""
    session_id_1 = seed_full_pipeline(tmp_path)
    run_helper(tmp_path, "--session", session_id_1)

    # Synthesize a second session summary with the same charter so the
    # same charter-coverage cross-reference fires against the same REQs.
    session_id_2 = "SESS-20260528-900"
    # The second session reuses session_id_1's body (charter coverage and
    # candidates are identical) but with a different SESS-ID in the title
    # and CS prefix so dedup logic gets exercised.
    summary_1 = workspace_file(tmp_path, f"sessions/{session_id_1}.md").read_text(
        encoding="utf-8"
    )
    summary_2 = summary_1.replace(session_id_1, session_id_2)
    # Replace CS-NNN-001 etc. with the second session's prefix so candidate IDs
    # remain unique.
    nnn_1 = session_id_1.split("-")[-1]
    nnn_2 = session_id_2.split("-")[-1]
    summary_2 = re.sub(rf"CS-{nnn_1}-(\d{{3}})", rf"CS-{nnn_2}-\1", summary_2)
    workspace_file(tmp_path, f"sessions/{session_id_2}.md").write_text(
        summary_2, encoding="utf-8"
    )

    run_helper(tmp_path, "--session", session_id_2)

    enriched = list_enriched_files(tmp_path)
    assert enriched, "expected at least one file enriched by both sessions"
    # At least one file must list both SESS-IDs.
    multi_enriched = []
    for path in enriched:
        fm, _ = split_frontmatter(path.read_text(encoding="utf-8"))
        sessions_line = frontmatter_scalar(fm, "phase_4_sessions") or ""
        ids = [s.strip() for s in sessions_line.strip("[]").split(",") if s.strip()]
        if session_id_1 in ids and session_id_2 in ids:
            multi_enriched.append((path, ids))
    assert multi_enriched, (
        "expected at least one file with both SESS-IDs in phase_4_sessions"
    )
    for path, ids in multi_enriched:
        assert ids == sorted(set(ids)), (
            f"{path.name}: phase_4_sessions not sorted-dedup ({ids})"
        )
        text = path.read_text(encoding="utf-8")
        # Per-session sub-blocks expected -- one per SESS-ID.
        assert f"### {session_id_1}" in text or session_id_1 in text
        assert f"### {session_id_2}" in text or session_id_2 in text


