def test_candidate_shape_contract_is_declared() -> None:
    """Per the Step 4.4 lesson on three-layer contracts: the consumer
    (enrich_test_ideas) must declare a CandidateScenario dataclass whose
    fields mirror the producer (session_summary.CandidateScenario).
    Importing the consumer module must therefore expose a matching dataclass.
    """
    sys.path.insert(0, str(SCRIPTS))
    try:
        import enrich_test_ideas
        import session_summary
    finally:
        if str(SCRIPTS) in sys.path:
            sys.path.remove(str(SCRIPTS))
    producer_fields = {
        f.name for f in session_summary.CandidateScenario.__dataclass_fields__.values()
    }
    consumer_fields = {
        f.name for f in enrich_test_ideas.CandidateScenario.__dataclass_fields__.values()
    }
    assert producer_fields <= consumer_fields, (
        f"consumer CandidateScenario must mirror producer fields. "
        f"Producer: {producer_fields}; consumer: {consumer_fields}"
    )
