def split_frontmatter(text: str) -> tuple[str, str]:
    """Return (frontmatter_body, post_frontmatter_body). Frontmatter is the
    YAML-ish block delimited by the first and second ``---`` lines."""
    assert text.startswith("---\n"), "test-idea seed must begin with --- frontmatter"
    end = text.find("\n---\n", 4)
    assert end > 0, "frontmatter must end with --- delimiter"
    return text[4:end], text[end + len("\n---\n") :]


def frontmatter_top_level_keys(fm: str) -> list[str]:
    """Return the top-level YAML keys in encounter order."""
    keys: list[str] = []
    for line in fm.split("\n"):
        m = re.match(r"^([a-z][a-z0-9_-]*):", line)
        if m:
            keys.append(m.group(1))
    return keys


def frontmatter_scalar(fm: str, key: str) -> str | None:
    """Return the trimmed scalar value for ``key`` if it appears as a
    single-line ``key: value`` entry; else None."""
    for line in fm.split("\n"):
        m = re.match(rf"^{re.escape(key)}:\s*(.*?)\s*$", line)
        if m:
            return m.group(1)
    return None


def list_enriched_files(project_root: Path) -> list[Path]:
    test_ideas = workspace_file(project_root, "test-ideas")
    return [
        p
        for p in sorted(test_ideas.glob("REQ-*.md"))
        if "## Phase 4 enrichment" in p.read_text(encoding="utf-8")
    ]


