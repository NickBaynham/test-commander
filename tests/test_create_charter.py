def test_idempotent_rerun_same_target(tmp_path: Path) -> None:
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    run_helper(tmp_path, "--target", "Sign-in flow")
    first = workspace_file(tmp_path, "charters/CH-001.md").read_bytes()

    proc = run_helper(tmp_path, "--target", "Sign-in flow")
    second = workspace_file(tmp_path, "charters/CH-001.md").read_bytes()
    assert first == second, "charter must be byte-identical on idempotent re-run"
    # CLI reports skip.
    stdout_lower = proc.stdout.lower()
    assert "skip" in stdout_lower or "already exists" in stdout_lower


def test_new_id_flag_reallocates(tmp_path: Path) -> None:
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    run_helper(tmp_path, "--target", "Sign-in flow")

    # Re-run with --new-id forces a fresh allocation even with the same target.
    run_helper(tmp_path, "--target", "Sign-in flow", "--new-id")

    ch_001 = workspace_file(tmp_path, "charters/CH-001.md")
    ch_002 = workspace_file(tmp_path, "charters/CH-002.md")
    assert ch_001.is_file(), "CH-001 must still exist"
    assert ch_002.is_file(), "CH-002 must be allocated on --new-id"


def test_user_edits_preserved_on_skip(tmp_path: Path) -> None:
    """When the helper skips an existing charter, user edits to the body
    must be preserved byte-for-byte. Mirrors the Phase 2 Step 2.6
    skip-not-overwrite idempotency contract for downstream-enriched
    artifacts."""
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    run_helper(tmp_path, "--target", "Sign-in flow")

    # Simulate a user edit.
    charter_path = workspace_file(tmp_path, "charters/CH-001.md")
    original = charter_path.read_text(encoding="utf-8")
    edited = original + "\n## User Notes\n\nManual edits live below the generated body.\n"
    charter_path.write_text(edited, encoding="utf-8")
    edited_bytes = charter_path.read_bytes()

    # Re-run idempotently.
    run_helper(tmp_path, "--target", "Sign-in flow")
    after_bytes = charter_path.read_bytes()
    assert after_bytes == edited_bytes, (
        "user edits must be preserved when the helper skips an existing charter"
    )


