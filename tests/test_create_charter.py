def test_auto_suggestion_ties_broken_alphabetically(tmp_path: Path) -> None:
    """When two entities have identical mention counts, the helper picks
    the alphabetically-earlier name. This guarantees deterministic output
    across machines and re-runs."""
    run_init(tmp_path)
    seed_minimal_phase_3_state(
        tmp_path,
        top_entity="Beta",  # 4 mentions total
        other_entities=("Alpha",),  # 1 mention - won't tie
    )
    # The seed mentions ``Beta`` heavily; override open-questions to make
    # Alpha tie with Beta on total mention count.
    workspace_file(tmp_path, "requirements/open-questions.md").write_text(
        "# Open questions\n\n"
        "- [tc-knowledge/learn-from-docs] Alpha is used but undefined.\n"
        "- [tc-knowledge/learn-from-docs] Alpha is used but undefined again.\n"
        "- [tc-knowledge/learn-from-docs] Alpha needs another mention.\n",
        encoding="utf-8",
    )
    # entities.md mentions Beta twice (## From documents + ## From code) plus
    # user-journey once = 3; Alpha 1 (entities) + 3 (open-questions) = 4.
    # Adjust entities so they tie at 4:4 (Beta: 3 entity sections + 1 journey;
    # Alpha: 1 entity + 3 open-questions). Actually let's make this simpler -
    # put both into entities.md and open-questions equally.
    workspace_file(tmp_path, "product-knowledge/entities.md").write_text(
        "# Entities\n\n## From documents\n\n"
        "- **Alpha** (file:1)\n"
        "- **Beta** (file:2)\n\n",
        encoding="utf-8",
    )
    workspace_file(tmp_path, "product-knowledge/user-journeys.md").write_text(
        "# User Journeys\n\n## From documents\n\n"
        "- **Sign in** (file:1) - 3 steps\n",
        encoding="utf-8",
    )
    workspace_file(tmp_path, "requirements/open-questions.md").write_text(
        "# Open questions\n\n"
        "- [tc-knowledge/learn-from-docs] Alpha is undefined.\n"
        "- [tc-knowledge/learn-from-docs] Beta is undefined.\n",
        encoding="utf-8",
    )
    run_helper(tmp_path)
    charter = workspace_file(tmp_path, "charters/CH-001.md").read_text(encoding="utf-8")
    fm = read_frontmatter(workspace_file(tmp_path, "charters/CH-001.md"))
    # Alphabetically earlier = Alpha.
    assert "Alpha" in fm["target"] or "Alpha" in charter, (
        f"expected Alpha (alphabetically earlier) to win the tie; got target={fm.get('target')}"
    )


