def test_risk_keywords_extension_applied(tmp_path: Path) -> None:
    """tc-explore.charters.risk-keywords extends the universal core
    additively. Adding a domain-specific risk keyword causes
    risk-register entries matching that keyword to surface as
    risk-areas in the generated charter."""
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)

    # Populate the risk-register with a project-specific risk entry.
    workspace_file(tmp_path, "risk-register/risk-register.md").write_text(
        "# Risk Register\n\n"
        "- PCI: storing primary account numbers in plaintext.\n"
        "- Session: token leakage via console logging.\n",
        encoding="utf-8",
    )

    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-explore:\n"
        "  charters:\n"
        "    risk-keywords: [PCI]\n",
        encoding="utf-8",
    )

    run_helper(tmp_path, "--target", "Sign-in flow")
    charter = workspace_file(tmp_path, "charters/CH-001.md").read_text(encoding="utf-8")
    # The PCI risk entry should surface as a risk area in the charter.
    assert "PCI" in charter, (
        "risk-keywords extension must surface project-specific risk entries"
    )


def test_area_keywords_extension_applied(tmp_path: Path) -> None:
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)

    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-explore:\n"
        "  charters:\n"
        "    area-keywords: [checkout]\n",
        encoding="utf-8",
    )

    # Provide a mission containing the extended area keyword.
    run_helper(tmp_path, "--mission", "Discover whether the checkout completes correctly.")
    charter = workspace_file(tmp_path, "charters/CH-001.md").read_text(encoding="utf-8")
    # The 'checkout' keyword should be recognized; behavior in v1 is to
    # accept the user's mission verbatim. The presence of the keyword in
    # the charter target is what the test asserts.
    assert "checkout" in charter.lower()
