def run_init(project_root: Path) -> None:
    subprocess.run(
        [sys.executable, str(INIT), str(project_root)],
        capture_output=True,
        text=True,
        check=True,
    )


def run_helper(
    project_root: Path,
    *args: str,
    expected_exit: int = 0,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        [sys.executable, str(HELPER), str(project_root), *args],
        capture_output=True,
        text=True,
    )
    assert proc.returncode == expected_exit, (
        f"helper exited {proc.returncode} (expected {expected_exit}). "
        f"stderr:\n{proc.stderr}\nstdout:\n{proc.stdout}"
    )
    return proc


def workspace_file(project_root: Path, rel: str) -> Path:
    return project_root / ".test-commander" / rel


def seed_minimal_phase_3_state(
    project_root: Path,
    *,
    top_entity: str = "Workspace",
    other_entities: tuple[str, ...] = ("Account", "Session", "Asset"),
) -> None:
    """Populate enough Phase 3 product-knowledge + Phase 2 open-questions
    state for the create-charter helper to satisfy its preconditions and
    pick ``top_entity`` as the highest-mention candidate."""
    workspace = project_root / ".test-commander"
    pk = workspace / "product-knowledge"
    pk.mkdir(parents=True, exist_ok=True)

    # Cross-cutting entities.md with one entity per source. Mention
    # ``top_entity`` more often by including it across multiple sources.
    entities = ["# Entities", "", "Cross-source entity index.", ""]
    entities.append("## From documents")
    entities.append("")
    for name in (top_entity, *other_entities):
        entities.append(f"- **{name}** (documents/uploaded/glossary.md:5)")
    entities.append("")
    entities.append("## From code")
    entities.append("")
    entities.append(f"- **{top_entity}** (documents/uploaded/code/app/models/workspace.py:4)")
    entities.append("")
    (pk / "entities.md").write_text("\n".join(entities) + "\n", encoding="utf-8")

    # user-journeys.md with at least one journey.
    (pk / "user-journeys.md").write_text(
        "# User Journeys\n\n"
        "Cross-source user-journey index.\n\n"
        "## From documents\n\n"
        f"- **Sign in and open a {top_entity}** "
        f"(documents/uploaded/user-journey-sign-in.md:1-23) - 7 steps\n",
        encoding="utf-8",
    )

    # system-model.md non-stub (the synthesizer's "ingested" marker shape).
    (pk / "system-model.md").write_text(
        "# System Model\n\n"
        "Auto-generated by the `tc-knowledge` synthesizer.\n\n"
        "## Sources ingested\n\n"
        "- **documents** - via `/tc:learn-from-docs`.\n",
        encoding="utf-8",
    )

    # Phase-2 open-questions mentioning the top entity more often.
    open_questions = workspace / "requirements" / "open-questions.md"
    open_questions.parent.mkdir(parents=True, exist_ok=True)
    open_questions.write_text(
        "# Open questions\n\n"
        f"- [tc-knowledge/learn-from-docs] Bolded term '{top_entity}' is used "
        "in prose but is not defined.\n"
        f"- [tc-knowledge/learn-from-code] [undocumented-function] Function "
        f"in {top_entity}.py has no docstring.\n"
        f"- [tc-knowledge/learn-from-tests] [untested-function] Public "
        f"function in {top_entity} module is not referenced by any test.\n"
        f"- [tc-knowledge/learn-from-docs] {other_entities[0]} relationship "
        "to other entities is unclear.\n",
        encoding="utf-8",
    )

    # risk-register stub - the helper reads but tolerates empty contents.
    risk_register = workspace / "risk-register" / "risk-register.md"
    risk_register.parent.mkdir(parents=True, exist_ok=True)
    risk_register.write_text(
        "# Risk Register\n\n_(empty until populated)_\n",
        encoding="utf-8",
    )


def read_frontmatter(charter_path: Path) -> dict[str, str]:
    text = charter_path.read_text(encoding="utf-8")
    m = re.match(r"\A---\n(.*?)\n---\n", text, re.DOTALL)
    assert m, f"missing frontmatter in {charter_path}"
    fm = m.group(1)
    result: dict[str, str] = {}
    for line in fm.split("\n"):
        if ":" not in line:
            continue
        if line.startswith(" "):
            # list-item continuation
            continue
        key, _, value = line.partition(":")
        result[key.strip()] = value.strip()
    return result


