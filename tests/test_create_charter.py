"""Step 4.2 - /tc:create-charter end-to-end tests.

Drives create_charter.py against a tmp consuming project seeded with
minimal Phase-3 product-knowledge state. Asserts:

- uninitialized workspace refused;
- empty / template-stub product-knowledge refused with a precondition
  error directing the user at ``/tc:learn-from-docs``;
- ``--target`` argument generates a CH-001 charter with valid YAML
  frontmatter carrying every CHARTER_REQUIRED_FIELDS key (the
  cross-phase contract from Step 4.1);
- auto-suggestion (no ``--target``) picks the entity with the highest
  total mention count across product-knowledge + open-questions
  (ties broken alphabetically);
- idempotent re-run with the same ``--target`` produces
  ``created: 0, skipped: 1`` and leaves the charter byte-identical;
- ``--new-id`` flag re-allocates as CH-002 even when the target
  matches an existing charter;
- user-edited charter body is preserved across re-runs (skip-not-
  overwrite for downstream-enriched artifacts; mirrors the Phase 2
  Step 2.6 idempotency lesson);
- the rendered charter passes the internal
  ``_charter_is_well_formed`` shape check;
- ``tc-explore.charters.{risk-keywords, area-keywords}`` config
  extensions union with the universal core additively.
"""

