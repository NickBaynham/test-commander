def test_entity_keywords_extension_applied(tmp_path: Path):
    """tc-knowledge.documents.entity-keywords surfaces domain terms as entities
    even when they appear only in prose, never under a glossary heading.

    The universal core matches capitalized noun phrases in glossary/entity
    sections only; the extension matcher is case-sensitive (domain entities
    are proper nouns), so this test uses an inline narrative with capitalized
    domain terms (Patient, Provider, Claim) rather than the lowercase
    common-noun mentions in the seeded fixture.
    """
    run_init(tmp_path)
    uploaded = workspace_file(tmp_path, "documents/uploaded")
    uploaded.mkdir(parents=True, exist_ok=True)
    (uploaded / "narrative.md").write_text(
        "# Domain narrative\n\n"
        "The Patient interacts with the Provider during a Claim event.\n"
        "Each Patient may file multiple Claims.\n",
        encoding="utf-8",
    )
    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-knowledge:\n"
        "  documents:\n"
        "    entity-keywords: [Patient, Provider, Claim]\n",
        encoding="utf-8",
    )
    run_helper(tmp_path)
    doc_model = workspace_file(tmp_path, "product-knowledge/documentation-model.md").read_text(
        encoding="utf-8"
    )
    for term in ("Patient", "Provider", "Claim"):
        assert term in doc_model, f"entity-keywords extension must surface '{term}' as an entity"


def test_journey_headings_extension_applied(tmp_path: Path):
    """config.yaml's tc-knowledge.documents.journey-headings widens journey-section detection."""
    run_init(tmp_path)
    uploaded = workspace_file(tmp_path, "documents/uploaded")
    uploaded.mkdir(parents=True, exist_ok=True)
    (uploaded / "story.md").write_text(
        "# Sign-in story\n\n"
        "## Story\n\n"
        "1. The user opens the app.\n"
        "2. The user signs in.\n"
        "3. The user reaches the dashboard.\n",
        encoding="utf-8",
    )
    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-knowledge:\n"
        "  documents:\n"
        "    journey-headings: [story]\n",
        encoding="utf-8",
    )
    run_helper(tmp_path)
    doc_model = workspace_file(tmp_path, "product-knowledge/documentation-model.md").read_text(
        encoding="utf-8"
    )
    # Without the extension the "Story" heading would not be a journey;
    # with the extension it is.
    assert "story.md" in doc_model, (
        "journey-headings extension must surface story.md as journey source"
    )
    journeys = workspace_file(tmp_path, "product-knowledge/user-journeys.md").read_text(
        encoding="utf-8"
    )
    assert "sign-in story" in journeys.lower() or "story" in journeys.lower()


