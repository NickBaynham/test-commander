"""Step 3.2 - /tc:learn-from-docs end-to-end tests.

Drives extract_knowledge_from_docs.py and the shared synthesize_system_model.py
helper against the seeded-sample-project fixture (Step 3.1). Asserts:

- uninitialized workspace refused;
- no-narrative-documents path writes a stub documentation-model + system-model;
- files containing ``REQ-\\d+`` markers are skipped (Phase-2 inverse filter);
- five positive rubric dimensions extracted with file:line provenance
  (entities, terms, user-journeys, business-rules, assumptions);
- two gap signals routed to ``requirements/open-questions.md``
  (undefined-term, contradictory-rule);
- cross-cutting artifacts get a ``## From documents`` section while preserving
  any prior ``## From <other-source>`` sections;
- ``system-model.md`` regenerated by the shared synthesizer;
- idempotent re-run: byte-identical documentation-model, byte-identical
  ``## From documents`` sections, line-stable open-questions, byte-identical
  ``system-model.md``;
- ``tc-knowledge.documents.entity-keywords`` and ``journey-headings`` config
  extensions union with the universal cores additively.
"""

from __future__ import annotations

import shutil
import subprocess
import sys
from pathlib import Path

import pytest

REPO = Path(__file__).resolve().parent.parent
HELPER = REPO / "plugins" / "test-commander" / "scripts" / "extract_knowledge_from_docs.py"
SYNTH = REPO / "plugins" / "test-commander" / "scripts" / "synthesize_system_model.py"
INIT = REPO / "plugins" / "test-commander" / "scripts" / "init_workspace.py"
FIXTURE_DOCS = REPO / "tests" / "fixtures" / "seeded-sample-project" / "documents"


def run_init(project_root: Path) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        [sys.executable, str(INIT), str(project_root)],
        capture_output=True,
        text=True,
        check=True,
    )


def run_helper(project_root: Path, expected_exit: int = 0) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        [sys.executable, str(HELPER), str(project_root)],
        capture_output=True,
        text=True,
    )
    assert proc.returncode == expected_exit, (
        f"helper exited {proc.returncode} (expected {expected_exit}). stderr:\n{proc.stderr}"
    )
    return proc


def copy_seeded_docs(project_root: Path) -> None:
    """Copy the three seeded fixture documents into the workspace's uploaded directory."""
    uploaded = project_root / ".test-commander" / "documents" / "uploaded"
    uploaded.mkdir(parents=True, exist_ok=True)
    for name in ("product-overview.md", "glossary.md", "user-journey-sign-in.md"):
        shutil.copy(FIXTURE_DOCS / name, uploaded / name)


def workspace_file(project_root: Path, rel: str) -> Path:
    return project_root / ".test-commander" / rel


