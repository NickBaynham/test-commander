"""Step 4.3 - /tc:explore end-to-end tests.

Drives explore.py against a tmp consuming project seeded with: (a) a
Phase 3 product-knowledge state so /tc:create-charter doesn't refuse,
(b) a CH-001 charter generated by /tc:create-charter against the same
target as the seeded fixture's charter.md, (c) a copy of the seeded
recorded-session.json placed at the configured recorded-path. Asserts:

- uninitialized workspace refused;
- missing charter refused with a precondition error pointing at
  ``/tc:create-charter``;
- missing recording refused with a precondition error pointing at the
  configured recorded path;
- seeded fixture generates ``exploration-notes/<SESS-ID>.md`` with the
  expected structure (Observations table + Anomalies summary + Evidence
  index + Charter-Coverage matrix);
- every required event type (page_load, click, fill, screenshot,
  console_message, network_request) is extracted and surfaces in the
  Observations table;
- every universal anomaly category (slow-response, console-error,
  broken-link, missing-evidence, auth-mismatch, unexpected-state)
  surfaces in the Anomalies summary;
- Evidence index lists every screenshot;
- at least one acceptance criterion is marked ``partial`` in the
  Charter-Coverage matrix (the seeded coverage shortfall - AC5
  about session expiration);
- the ``missing-evidence`` gap signal routes to
  ``requirements/open-questions.md`` with the ``[exploration-review]``
  prefix for the seeded anomaly that has ``screenshot_id: null`` AND
  no screenshot event within ±3s;
- ``--no-review`` suppresses the review sub-mode (no
  ``[exploration-review]`` gap signals appended);
- ``tc-explore.mode: live`` is refused under pytest with the same
  ``LiveModeRefusedError`` shape established in Phase 3 Step 3.5;
- idempotent re-run produces byte-identical exploration note;
- ``tc-explore.exploration.recorded-path`` config extension points the
  helper at a custom recording location;
- session IDs follow the ``SESS-YYYYMMDD-NNN`` format.
"""

from __future__ import annotations

import re
import shutil
import subprocess
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
HELPER = REPO / "plugins" / "test-commander" / "scripts" / "explore.py"
INIT = REPO / "plugins" / "test-commander" / "scripts" / "init_workspace.py"
CHARTER_HELPER = REPO / "plugins" / "test-commander" / "scripts" / "create_charter.py"
FIXTURE_DIR = REPO / "tests" / "fixtures" / "seeded-exploration-session"
FIXTURE_RECORDED_SESSION = FIXTURE_DIR / "recorded-session.json"

# Charter target string used in the helper invocation. Must match the
# seeded charter.md's target verbatim so the auto-suggested coverage
# heuristic operates against the right ACs.
CHARTER_TARGET = (
    "Sign-in flow plus workspace-detail asset upload (POST /workspaces/{id}/assets)."
)

SESSION_ID_RE = re.compile(r"^SESS-\d{8}-\d{3}$")


# ---------------------------------------------------------------------------
# CLI + setup helpers
# ---------------------------------------------------------------------------


def run_init(project_root: Path) -> None:
    subprocess.run(
        [sys.executable, str(INIT), str(project_root)],
        capture_output=True,
        text=True,
        check=True,
    )


def run_helper(
    project_root: Path,
    *args: str,
    expected_exit: int = 0,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        [sys.executable, str(HELPER), str(project_root), *args],
        capture_output=True,
        text=True,
    )
    assert proc.returncode == expected_exit, (
        f"helper exited {proc.returncode} (expected {expected_exit}). "
        f"stderr:\n{proc.stderr}\nstdout:\n{proc.stdout}"
    )
    return proc


def run_create_charter(project_root: Path, target: str) -> None:
    proc = subprocess.run(
        [sys.executable, str(CHARTER_HELPER), str(project_root), "--target", target],
        capture_output=True,
        text=True,
    )
    assert proc.returncode == 0, (
        f"create_charter failed: stderr:\n{proc.stderr}\nstdout:\n{proc.stdout}"
    )


def workspace_file(project_root: Path, rel: str) -> Path:
    return project_root / ".test-commander" / rel


def seed_minimal_phase_3_state(project_root: Path) -> None:
    """Populate enough product-knowledge state for create_charter to satisfy
    its precondition check (mirrors the helper in test_create_charter.py)."""
    workspace = project_root / ".test-commander"
    pk = workspace / "product-knowledge"
    pk.mkdir(parents=True, exist_ok=True)

    (pk / "entities.md").write_text(
        "# Entities\n\n## From documents\n\n"
        "- **Account** (file:1)\n"
        "- **Workspace** (file:2)\n"
        "- **Session** (file:3)\n",
        encoding="utf-8",
    )
    (pk / "user-journeys.md").write_text(
        "# User Journeys\n\n## From documents\n\n"
        "- **Sign in and open a workspace** (file:1) - 7 steps\n",
        encoding="utf-8",
    )
    (pk / "system-model.md").write_text(
        "# System Model\n\n## Sources ingested\n\n"
        "- **documents** - via `/tc:learn-from-docs`.\n",
        encoding="utf-8",
    )

    open_q = workspace / "requirements" / "open-questions.md"
    open_q.parent.mkdir(parents=True, exist_ok=True)
    open_q.write_text("# Open questions\n\n", encoding="utf-8")


def seed_for_explore(project_root: Path, *, target: str = CHARTER_TARGET) -> Path:
    """Set up workspace with charter + recorded session at the default path."""
    run_init(project_root)
    seed_minimal_phase_3_state(project_root)
    run_create_charter(project_root, target)

    recorded_dir = workspace_file(project_root, "documents/uploaded/recorded-sessions")
    recorded_dir.mkdir(parents=True, exist_ok=True)
    target_file = recorded_dir / "CH-001.json"
    shutil.copy(FIXTURE_RECORDED_SESSION, target_file)
    return target_file


def find_session_note(project_root: Path) -> Path:
    notes_dir = workspace_file(project_root, "exploration-notes")
    matches = list(notes_dir.glob("SESS-*.md"))
    assert matches, f"expected an exploration note under {notes_dir}; found none"
    assert len(matches) == 1, f"expected exactly one session note; found {len(matches)}"
    return matches[0]


