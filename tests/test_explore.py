def test_helper_file_exists() -> None:
    assert HELPER.is_file(), f"missing helper: {HELPER.relative_to(REPO)}"


def test_uninitialized_workspace_refused(tmp_path: Path) -> None:
    proc = subprocess.run(
        [sys.executable, str(HELPER), str(tmp_path), "--charter", "CH-001"],
        capture_output=True,
        text=True,
    )
    assert proc.returncode == 2, proc.stderr


def test_missing_charter_refused(tmp_path: Path) -> None:
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    # No charter created. Run /tc:explore - should refuse.
    proc = run_helper(tmp_path, "--charter", "CH-001", expected_exit=2)
    stderr_lower = proc.stderr.lower()
    assert "charter" in stderr_lower
    assert "/tc:create-charter" in proc.stderr


def test_missing_recording_refused(tmp_path: Path) -> None:
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    run_create_charter(tmp_path, CHARTER_TARGET)
    # Charter exists but no recording at the default path.
    proc = run_helper(tmp_path, "--charter", "CH-001", expected_exit=2)
    stderr_lower = proc.stderr.lower()
    assert "recorded" in stderr_lower or "recording" in stderr_lower


