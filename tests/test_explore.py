def test_idempotent_rerun_byte_identical(tmp_path: Path) -> None:
    seed_for_explore(tmp_path)
    run_helper(tmp_path, "--charter", "CH-001")
    first_note = find_session_note(tmp_path).read_bytes()
    first_open_q = workspace_file(tmp_path, "requirements/open-questions.md").read_text(
        encoding="utf-8"
    )

    run_helper(tmp_path, "--charter", "CH-001")
    second_note = find_session_note(tmp_path).read_bytes()
    second_open_q = workspace_file(tmp_path, "requirements/open-questions.md").read_text(
        encoding="utf-8"
    )

    assert first_note == second_note, (
        "exploration note must be byte-identical on re-run against the same recording"
    )
    assert first_open_q.count("\n") == second_open_q.count("\n"), (
        "open-questions.md line count must be stable across re-runs (dedup contract)"
    )


