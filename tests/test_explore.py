def test_recorded_path_extension_applied(tmp_path: Path) -> None:
    """tc-explore.exploration.recorded-path overrides the default
    documents/uploaded/recorded-sessions/<CH-ID>.json path."""
    run_init(tmp_path)
    seed_minimal_phase_3_state(tmp_path)
    run_create_charter(tmp_path, CHARTER_TARGET)

    # Place recording at a non-default path.
    custom_dir = workspace_file(tmp_path, "fixtures/recordings")
    custom_dir.mkdir(parents=True, exist_ok=True)
    shutil.copy(FIXTURE_RECORDED_SESSION, custom_dir / "CH-001.json")

    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-explore:\n  exploration:\n"
          "    recorded-path: fixtures/recordings\n",
        encoding="utf-8",
    )
    run_helper(tmp_path, "--charter", "CH-001")
    # Session note exists - the helper found the recording at the custom path.
    find_session_note(tmp_path)


def test_session_id_format(tmp_path: Path) -> None:
    seed_for_explore(tmp_path)
    run_helper(tmp_path, "--charter", "CH-001")
    note = find_session_note(tmp_path)
    assert SESSION_ID_RE.match(note.stem), (
        f"session ID {note.stem!r} must match SESS-YYYYMMDD-NNN"
    )
