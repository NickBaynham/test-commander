def test_charter_coverage_matrix_present(tmp_path: Path) -> None:
    seed_for_explore(tmp_path)
    run_helper(tmp_path, "--charter", "CH-001")
    note_text = find_session_note(tmp_path).read_text(encoding="utf-8")
    # Coverage matrix renders each AC with one of three verdicts:
    # observed / partial / unobserved.
    assert "observed" in note_text.lower()
    # There must be at least one verdict-bearing row.
    assert re.search(
        r"\b(observed|partial|unobserved)\b", note_text, flags=re.IGNORECASE,
    ), "Charter-Coverage matrix must mark each AC with a verdict"


def test_charter_coverage_marks_partial(tmp_path: Path) -> None:
    """The seeded charter's AC about session expiration is not actually
    covered by the seeded recording (the events never trigger an actual
    session expiration; only a sign-out attempt). The coverage assessor
    should mark this AC as ``partial`` because /sign-in is reachable
    but the trigger word ``expiration`` is not observed."""
    seed_for_explore(tmp_path)
    run_helper(tmp_path, "--charter", "CH-001")
    note_text = find_session_note(tmp_path).read_text(encoding="utf-8")
    assert "partial" in note_text.lower(), (
        "at least one acceptance criterion must be marked 'partial' "
        "for the seeded coverage shortfall (AC5 about session expiration)"
    )


