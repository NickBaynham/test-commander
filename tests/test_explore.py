def test_missing_evidence_gap_routes_to_open_questions(tmp_path: Path) -> None:
    """The seeded recording has one anomaly with screenshot_id: null and
    no screenshot event within ±3s of its timestamp. The review sub-mode
    should emit a [exploration-review] gap signal naming missing-evidence."""
    seed_for_explore(tmp_path)
    run_helper(tmp_path, "--charter", "CH-001")
    open_q = workspace_file(tmp_path, "requirements/open-questions.md").read_text(
        encoding="utf-8"
    )
    lowered = open_q.lower()
    assert "[exploration-review]" in lowered
    assert "missing-evidence" in lowered or "missing evidence" in lowered


def test_no_review_suppresses_review_sub_mode(tmp_path: Path) -> None:
    """The --no-review flag suppresses the review sub-mode entirely; no
    [exploration-review] gap signals should be appended."""
    seed_for_explore(tmp_path)
    # Snapshot open-questions before the run.
    open_q_path = workspace_file(tmp_path, "requirements/open-questions.md")
    pre = open_q_path.read_text(encoding="utf-8") if open_q_path.is_file() else ""

    run_helper(tmp_path, "--charter", "CH-001", "--no-review")
    post = open_q_path.read_text(encoding="utf-8")
    # The session note is still written.
    find_session_note(tmp_path)
    # No new [exploration-review] entries.
    assert "[exploration-review]" not in post.lower(), (
        "--no-review must suppress every [exploration-review] open-question append"
    )
    # Pre-existing content is preserved.
    assert post.startswith(pre[: min(len(pre), 50)])


