# Live-mode refusal under pytest
# ---------------------------------------------------------------------------


def test_live_mode_refused_under_pytest(tmp_path: Path) -> None:
    """tc-explore.mode: live must refuse when PYTEST_CURRENT_TEST is set
    so no MCP connection or browser launch leaks from the suite."""
    seed_for_explore(tmp_path)
    config = workspace_file(tmp_path, "config.yaml")
    base = config.read_text(encoding="utf-8") if config.is_file() else ""
    config.write_text(
        base.rstrip()
        + "\n\ntc-explore:\n  exploration:\n    mode: live\n"
          "    mcp-endpoint: \"http://localhost:9999\"\n"
          "    target-url: \"http://localhost:8000\"\n",
        encoding="utf-8",
    )
    proc = run_helper(tmp_path, "--charter", "CH-001", expected_exit=2)
    stderr_lower = proc.stderr.lower()
    assert "live mode" in stderr_lower or "refused" in stderr_lower


