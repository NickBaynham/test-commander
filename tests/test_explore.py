def test_seeded_recording_generates_session_note(tmp_path: Path) -> None:
    seed_for_explore(tmp_path)
    proc = run_helper(tmp_path, "--charter", "CH-001")
    session_note = find_session_note(tmp_path)
    assert session_note.is_file()
    assert SESSION_ID_RE.match(session_note.stem), (
        f"session ID must match SESS-YYYYMMDD-NNN; got {session_note.stem}"
    )
    # CLI announces success.
    stdout_lower = proc.stdout.lower()
    assert "exploration" in stdout_lower or "session" in stdout_lower


def test_session_note_has_required_sections(tmp_path: Path) -> None:
    seed_for_explore(tmp_path)
