def _format_decorator(node: ast.expr) -> str:
    """Render a decorator expression back to a source-like string."""
    if isinstance(node, ast.Name):
        return f"@{node.id}"
    if isinstance(node, ast.Attribute):
        parts: list[str] = [node.attr]
        current = node.value
        while isinstance(current, ast.Attribute):
            parts.append(current.attr)
            current = current.value
        if isinstance(current, ast.Name):
            parts.append(current.id)
        return "@" + ".".join(reversed(parts))
    if isinstance(node, ast.Call):
        return _format_decorator(node.func) + "(...)"
    return "@<expr>"


def _attributes_from_init(class_node: ast.ClassDef) -> tuple[str, ...]:
    attrs: list[str] = []
    for member in class_node.body:
        is_init_def = isinstance(member, (ast.FunctionDef, ast.AsyncFunctionDef))
        if is_init_def and member.name == "__init__":
            for stmt in member.body:
                if isinstance(stmt, ast.Assign):
                    for target in stmt.targets:
                        if (
                            isinstance(target, ast.Attribute)
                            and isinstance(target.value, ast.Name)
                            and target.value.id == "self"
                        ):
                            attrs.append(target.attr)
                elif (
                    isinstance(stmt, ast.AnnAssign)
                    and isinstance(stmt.target, ast.Attribute)
                    and isinstance(stmt.target.value, ast.Name)
                    and stmt.target.value.id == "self"
                ):
                    attrs.append(stmt.target.attr)
            break
    return tuple(attrs)


def extract_python(path: Path, rel_path: str) -> tuple[Module | None, list[Class], list[Function]]:
    text = path.read_text(encoding="utf-8")
    try:
        tree = ast.parse(text, filename=str(path))
    except SyntaxError:
        return None, [], []

    module = Module(
        rel_path=rel_path,
        line_count=text.count("\n") + 1,
        docstring=ast.get_docstring(tree),
    )

    classes: list[Class] = []
    functions: list[Function] = []
    for node in tree.body:
        if isinstance(node, ast.ClassDef):
            bases: list[str] = []
            for base in node.bases:
                if isinstance(base, ast.Name):
                    bases.append(base.id)
                elif isinstance(base, ast.Attribute):
                    bases.append(_format_decorator(base).lstrip("@"))
            classes.append(
                Class(
                    name=node.name,
                    docstring=ast.get_docstring(node),
                    bases=tuple(bases),
                    attributes=_attributes_from_init(node),
                    source_file=rel_path,
                    line=node.lineno,
                )
            )
            # Also capture top-level methods as functions (with their class qualified name).
            for member in node.body:
                is_method = isinstance(member, (ast.FunctionDef, ast.AsyncFunctionDef))
                if is_method and not member.name.startswith("_"):
                    functions.append(
                        Function(
                            name=f"{node.name}.{member.name}",
                            docstring=ast.get_docstring(member),
                            decorators=tuple(_format_decorator(d) for d in member.decorator_list),
                            source_file=rel_path,
                            line=member.lineno,
                            is_async=isinstance(member, ast.AsyncFunctionDef),
                        )
                    )
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            functions.append(
                Function(
                    name=node.name,
                    docstring=ast.get_docstring(node),
                    decorators=tuple(_format_decorator(d) for d in node.decorator_list),
                    source_file=rel_path,
                    line=node.lineno,
                    is_async=isinstance(node, ast.AsyncFunctionDef),
                )
            )

    return module, classes, functions


