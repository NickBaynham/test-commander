@dataclass(frozen=True)
class Module:
    rel_path: str
    line_count: int
    docstring: str | None


@dataclass(frozen=True)
class Class:
    name: str
    docstring: str | None
    bases: tuple[str, ...]
    attributes: tuple[str, ...]
    source_file: str
    line: int


@dataclass(frozen=True)
class Function:
    name: str
    docstring: str | None
    decorators: tuple[str, ...]
    source_file: str
    line: int
    is_async: bool


@dataclass(frozen=True)
class GapSignal:
    kind: str
    description: str
    source_file: str | None
    line: int | None


@dataclass
class Extensions:
    source_root: str = DEFAULT_SOURCE_ROOT
    enabled_languages: tuple[str, ...] = DEFAULT_ENABLED_LANGUAGES
    ignored_paths: tuple[str, ...] = DEFAULT_IGNORED_PATHS


@dataclass
class CodeFindings:
    modules: list[Module] = field(default_factory=list)
    classes: list[Class] = field(default_factory=list)
    functions: list[Function] = field(default_factory=list)
    unsupported_files: list[tuple[str, str]] = field(default_factory=list)  # (rel_path, ext)
    gaps: list[GapSignal] = field(default_factory=list)


