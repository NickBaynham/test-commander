def render_code_entities_section(findings: CodeFindings) -> str:
    if not findings.classes:
        return "_No classes extracted from code._"
    lines: list[str] = []
    for cls in sorted(findings.classes, key=lambda c: (c.name, c.source_file, c.line)):
        summary = _first_line(cls.docstring)
        suffix = f" - {summary}" if summary else ""
        lines.append(f"- **{cls.name}** ({cls.source_file}:{cls.line}){suffix}")
    return "\n".join(lines)


