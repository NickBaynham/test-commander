def _is_ignored(path: Path, root: Path, ignored: Iterable[str]) -> bool:
    rel_parts = path.relative_to(root).parts
    return any(part in ignored for part in rel_parts) or any(
        token in str(path.relative_to(root)) for token in ignored
    )


def discover_python(root: Path, ignored: Iterable[str]) -> list[Path]:
    if not root.is_dir():
        return []
    found: list[Path] = []
    for path in sorted(root.rglob("*.py")):
        if not path.is_file():
            continue
        if _is_ignored(path, root, ignored):
            continue
        found.append(path)
    return found


def discover_unsupported(root: Path, ignored: Iterable[str]) -> list[Path]:
    if not root.is_dir():
        return []
    found: list[Path] = []
    for path in sorted(root.rglob("*")):
        if not path.is_file():
            continue
        if path.suffix.lower() not in UNSUPPORTED_LANG_EXTENSIONS:
            continue
        if _is_ignored(path, root, ignored):
            continue
        found.append(path)
    return found


