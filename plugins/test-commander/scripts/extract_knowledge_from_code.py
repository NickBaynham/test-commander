def _first_line(docstring: str | None) -> str:
    if not docstring:
        return ""
    return docstring.strip().splitlines()[0]


def render_code_model(findings: CodeFindings) -> str:
    lines: list[str] = []
    lines.append("# Code-Derived Model")
    lines.append("")
    lines.append(
        "Auto-generated by `/tc:learn-from-code`. Re-running overwrites this "
        "file byte-deterministically. Edits will not survive a re-run."
    )
    lines.append("")

    lines.append("## Source modules")
    lines.append("")
    if not findings.modules and not findings.unsupported_files:
        lines.append("_No code source found._")
        lines.append("")
        lines.append(
            "Configure the source root via `tc-knowledge.code.source-root` in "
            "`<workspace>/config.yaml`. Default: `documents/uploaded/code`. "
            "Recognized v1 language: Python."
        )
        lines.append("")
        return "\n".join(lines).rstrip("\n") + "\n"

    if findings.modules:
        lines.append("| Module | Lines |")
        lines.append("| --- | --- |")
        for module in sorted(findings.modules, key=lambda m: m.rel_path):
            lines.append(f"| {module.rel_path} | {module.line_count} |")
    else:
        lines.append("_No Python modules parsed._")
    lines.append("")

    # Classes
    lines.append("## Classes")
    lines.append("")
    if findings.classes:
        lines.append("| Class | Attributes | Source |")
        lines.append("| --- | --- | --- |")
        for cls in sorted(findings.classes, key=lambda c: (c.name, c.source_file, c.line)):
            attrs = ", ".join(cls.attributes) if cls.attributes else ""
            lines.append(f"| {cls.name} | {attrs} | {cls.source_file}:{cls.line} |")
        lines.append("")
        for cls in sorted(findings.classes, key=lambda c: (c.name, c.source_file)):
            summary = _first_line(cls.docstring)
            if summary:
                lines.append(f"- **{cls.name}** ({cls.source_file}:{cls.line}) - {summary}")
        if any(_first_line(c.docstring) for c in findings.classes):
            lines.append("")
    else:
        lines.append("_No classes extracted._")
        lines.append("")

    # Functions
    lines.append("## Functions")
    lines.append("")
    if findings.functions:
        lines.append("| Function | Decorators | Source |")
        lines.append("| --- | --- | --- |")
        for fn in sorted(findings.functions, key=lambda f: (f.name, f.source_file, f.line)):
            decorators = ", ".join(fn.decorators) if fn.decorators else ""
            kind = "async " if fn.is_async else ""
            lines.append(
                f"| {kind}{fn.name} | {decorators} | {fn.source_file}:{fn.line} |"
            )
        lines.append("")
        for fn in sorted(findings.functions, key=lambda f: (f.name, f.source_file)):
            summary = _first_line(fn.docstring)
            if summary:
                lines.append(f"- **{fn.name}** ({fn.source_file}:{fn.line}) - {summary}")
        if any(_first_line(f.docstring) for f in findings.functions):
            lines.append("")
    else:
        lines.append("_No functions extracted._")
        lines.append("")

    # Languages detected but not parsed
    lines.append("## Languages detected but not parsed in v1")
    lines.append("")
    if findings.unsupported_files:
        lines.append("| File | Extension |")
        lines.append("| --- | --- |")
        for rel_path, ext_suffix in sorted(findings.unsupported_files):
            lines.append(f"| {rel_path} | {ext_suffix} |")
    else:
        lines.append("_No non-Python source files detected._")
    lines.append("")

    # Gap signals
    lines.append("## Gap signals (routed to `requirements/open-questions.md`)")
    lines.append("")
    if findings.gaps:
        for gap in sorted(
            findings.gaps,
            key=lambda g: (g.kind, g.source_file or "", g.line or 0),
        ):
            citation = ""
            if gap.source_file and gap.line:
                citation = f" ({gap.source_file}:{gap.line})"
            lines.append(f"- **{gap.kind}**: {gap.description}{citation}")
    else:
        lines.append("_No gap signals detected._")
    lines.append("")

    return "\n".join(lines).rstrip("\n") + "\n"


