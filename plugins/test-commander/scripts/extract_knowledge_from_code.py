def run(project_root: Path) -> int:
    workspace = workspace_dir(project_root)
    ext = load_extensions(workspace)
    findings = aggregate(workspace, ext)

    # 1. code-derived-model.md
    code_model = workspace / "product-knowledge" / "code-derived-model.md"
    code_model.parent.mkdir(parents=True, exist_ok=True)
    code_model.write_text(render_code_model(findings), encoding="utf-8")

    # 2. cross-cutting section-overwrites (entities; business-rules only when
    # render produces a non-empty body).
    update_cross_cutting(workspace, "entities.md", render_code_entities_section(findings))
    update_cross_cutting(
        workspace, "business-rules.md", render_code_rules_section(findings)
    )

    # 3. open-questions
    append_open_questions(workspace, findings.gaps)

    # 4. synthesizer
    synth_mod.synthesize(project_root)

    return 0


def main(argv: Iterable[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Extract knowledge from Python source under the configured root "
            "(default <workspace>/documents/uploaded/code/) and populate "
            "<workspace>/product-knowledge/."
        ),
    )
    parser.add_argument(
        "project_root",
        nargs="?",
        default=".",
        help="Project root (default: current directory).",
    )
    args = parser.parse_args(list(argv) if argv is not None else None)
    project_root = Path(args.project_root).resolve()

    try:
        return run(project_root)
    except UninitializedWorkspaceError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2


if __name__ == "__main__":
    raise SystemExit(main())
