SPEC_ENDPOINT_ROW_RE = re.compile(
    r"^\|\s*([A-Z]+)\s+(\S[^|]*?)\s*\|\s*([^|]*?)\s*\|"
)


def parse_spec_endpoints(workspace: Path) -> list[tuple[str, str, str]]:
    """Return [(method, path, operation_id), ...] from spec-derived-model.md.

    Returns an empty list when the file is still the workspace-template stub
    or carries any helper-emitted empty-run sentinel.
    """
    spec_model = workspace / "product-knowledge" / "spec-derived-model.md"
    if not spec_model.is_file():
        return []
    text = spec_model.read_text(encoding="utf-8")
    if SPEC_MODEL_GENERATED_MARKER not in text:
        return []
    if "no spec found" in text.lower():
        return []

    endpoints: list[tuple[str, str, str]] = []
    in_endpoints_section = False
    for line in text.splitlines():
        if line.startswith("## Endpoints"):
            in_endpoints_section = True
            continue
        if line.startswith("## ") and in_endpoints_section:
            break
        if not in_endpoints_section:
            continue
        m = SPEC_ENDPOINT_ROW_RE.match(line)
        if not m:
            continue
        method, path, operation = m.group(1).strip(), m.group(2).strip(), m.group(3).strip()
        if method == "Endpoint" or method.startswith("-"):
            continue
        endpoints.append((method, path, operation))
    return endpoints


def detect_unimplemented_endpoints(
    workspace: Path, functions: list[Function]
) -> list[GapSignal]:
    """Cross-check spec endpoints against parsed function names.

    Match rule: an endpoint is considered implemented iff its
    ``operationId`` equals any plain function name (the qualified
    ``Class.method`` form is also matched against its trailing segment).
    """
    endpoints = parse_spec_endpoints(workspace)
    if not endpoints:
        return []
    function_names: set[str] = set()
    for fn in functions:
        function_names.add(fn.name)
        if "." in fn.name:
            function_names.add(fn.name.split(".", 1)[1])
    gaps: list[GapSignal] = []
    for method, path, operation in endpoints:
        if not operation:
            continue  # cannot match without an operationId.
        if operation in function_names:
            continue
        gaps.append(
            GapSignal(
                kind="unimplemented-endpoint",
                description=(
                    f"Spec endpoint {method} {path} (operationId "
                    f"'{operation}') has no matching function in the parsed "
                    "code"
                ),
                source_file=None,
                line=None,
            )
        )
    return gaps


