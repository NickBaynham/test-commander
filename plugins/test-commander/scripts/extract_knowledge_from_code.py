def aggregate(workspace: Path, ext: Extensions) -> CodeFindings:
    source_root = (workspace / ext.source_root).resolve()
    findings = CodeFindings()

    if "python" not in (lang.lower() for lang in ext.enabled_languages):
        # v1 only parses Python; an empty enabled-languages list means no parse.
        return findings

    for path in discover_python(source_root, ext.ignored_paths):
        rel_path = str(path.relative_to(workspace))
        module, classes, functions = extract_python(path, rel_path)
        if module is not None:
            findings.modules.append(module)
        findings.classes.extend(classes)
        findings.functions.extend(functions)

    for path in discover_unsupported(source_root, ext.ignored_paths):
        rel_path = str(path.relative_to(workspace))
        ext_suffix = path.suffix.lower()
        findings.unsupported_files.append((rel_path, ext_suffix))
        findings.gaps.append(
            GapSignal(
                kind="language-unsupported-in-v1",
                description=(
                    f"File {rel_path} has extension {ext_suffix}; the helper "
                    "detects the language but does not parse it in v1"
                ),
                source_file=rel_path,
                line=1,
            )
        )

    # undocumented-function gap (public name without a docstring).
    for fn in findings.functions:
        if fn.name.startswith("_"):
            continue
        if "." in fn.name and fn.name.split(".", 1)[1].startswith("_"):
            continue
        if fn.docstring is not None and fn.docstring.strip():
            continue
        findings.gaps.append(
            GapSignal(
                kind="undocumented-function",
                description=(
                    f"Public function '{fn.name}' in {fn.source_file} has no "
                    "docstring"
                ),
                source_file=fn.source_file,
                line=fn.line,
            )
        )

    # unimplemented-endpoint gap (only when spec-derived-model.md is generated).
    findings.gaps.extend(detect_unimplemented_endpoints(workspace, findings.functions))

    return findings


