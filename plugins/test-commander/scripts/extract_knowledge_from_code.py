#!/usr/bin/env python3
"""/tc:learn-from-code helper - Phase 3 Step 3.4.

Walks the configured source root (default ``<workspace>/documents/uploaded/
code/``) and extracts Python modules, classes, functions, docstrings, and
decorators using the stdlib ``ast`` module. Non-Python source files
(``.ts/.tsx/.js/.jsx/.go/.java/.rb``) are detected by extension and counted
as ``language-unsupported-in-v1`` gaps - never silently ignored.

Writes:

- ``<workspace>/product-knowledge/code-derived-model.md`` - overwritten
  byte-deterministically.
- The ``## From code`` section in ``entities.md`` (classes contribute as
  domain entities). ``business-rules.md`` receives a ``## From code``
  section only if module-level constants carry an explicit rule docstring;
  the section is omitted otherwise. ``user-journeys.md`` and
  ``assumptions.md`` are NOT touched.
- Gap-signal questions appended to
  ``<workspace>/requirements/open-questions.md`` with the Phase-2 dedup
  contract (``source-id, question-text``).
- ``<workspace>/product-knowledge/system-model.md`` regenerated via the
  shared ``synthesize_system_model.py`` helper.

Per D18 the helper ships inside the plugin. Per D19 the language detector
and the keyword sets are universal cores; projects extend via
``tc-knowledge.code:`` in ``<workspace>/config.yaml``.

Exit codes:
    0 - code-derived-model written.
    2 - uninitialized workspace.
"""

